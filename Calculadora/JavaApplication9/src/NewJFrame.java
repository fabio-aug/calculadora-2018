    public static Double E(String e) {
        // E --> T + E | T - E | T
        Double numero1 = T(e), numero2;
        char caractere = e.charAt(p);
        if (caractere == ')') {
            return numero1;
        }
        if (p < e.length() && numero1 != null) {
            if (caractere == '+') {
                p = p + 1;
                numero2 = E(e);
                if (numero2 != null) {
                    return numero1 + numero2;
                }
            } else if (caractere == '-') {
                numero2 = E(e);
                if (numero2 != null) {
                    return numero1 + numero2;
                }
            }
        }
        return numero1;
    }

    public static Double T(String e) {
        // T --> F * T | F
        Double numero1 = F(e), numero2;
        char caractere = e.charAt(p);
        if (caractere == ')') {
            p = p + 1;
            return numero1;
        }
        if (p < e.length() && numero1 != null) {
            if (caractere == '*') {
                p = p + 1;
                numero2 = T(e);
                if (numero2 != null) {
                    return numero1 * numero2;
                }
            } else if (caractere == '÷') {
                p = p + 1;
                numero2 = T(e);
                if (numero2 != null) {
                    return (numero1 * 1) / numero2;
                }
            }
        }
        return numero1;
    }

    public static Double F(String e) {
        Double numero1 = G(e), numero2;
        char caractere = e.charAt(p);
        if (caractere == ')') {
            p = p + 1;
            return numero1;
        }
        if (p < e.length() && numero1 != null) {
            if (caractere == '√') {
                p = p + 1;
                numero2 = F(e);
                if (numero2 != null) {
                    return numero1 * Math.sqrt(numero2);
                }
            } else if (caractere == '^') {
                p = p + 1;
                numero2 = F(e);
                if (numero2 != null) {
                    return Math.pow(numero1, numero2);
                }
            } else if (caractere == '%') {
                p = p + 1;
                numero2 = F(e);
                if (numero2 == null) {
                    return numero1 / 100;
                } else if (numero2 != null) {
                    return (numero1 * numero2 / 100);
                }
            }
        } else if (caractere == '√') {
            p = p + 1;
            numero2 = F(e);
            if (numero2 != null) {
                return Math.sqrt(numero2);
            }
        } else if (caractere == '-') {
            p = p + 1;
            numero2 = F(e);
            if (numero2 != null) {
                String negativo = "-";
                String contatenei = negativo + numero2;
                Double vai = new Double(contatenei);
                return vai;
            }
        }
        return numero1;
    }

    public static Double G(String e) {
        Double numero1 = H(e), numero2;
        char caractere = e.charAt(p);
        if (caractere == '(') {
            p = p + 1;
            return E(e);
        }
        return numero1;
    }

    public static Double H(String e) {
        // F --> num
        String texto = "";
        Double numero = null;
        boolean temPontinho = false;
        boolean temNumero = false;
        char caractere = e.charAt(p);
        while (Character.isDigit(caractere) || caractere == '.') {
            if (temPontinho == false && caractere == '.') {
                texto = texto + caractere;
                temPontinho = true;
            } else {
                if (Character.isDigit(caractere)) {
                    texto = texto + caractere;
                }
            }
            p = p + 1;
            caractere = e.charAt(p);
            temNumero = true;
        }
        if (temNumero) {
            numero = new Double(texto);
        } else {
            if (temPontinho) {
                numero = new Double(texto);
            }
        }
        return numero;
    }
