    private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton25ActionPerformed
        double delta = 0.0;
        if (!jTextField1.getText().equals("") || !jTextField2.getText().equals("") || !jTextField3.getText().equals("")) {
            String textoBaskara;
            double a = new Double(jTextField1.getText().equals("") ? "0.0" : jTextField1.getText());
            double b = new Double(jTextField1.getText().equals("") ? "0.0" : jTextField2.getText());
            double c = new Double(jTextField3.getText().equals("") ? "0.0" : jTextField3.getText());

            if (a == 0) {
                textoBaskara = a + "x² + " + b + "x + " + c + " = 0 nem é equação do segundo grau!";
            } else {
                delta = Math.pow(b, 2) - 4 * a * c;
                if (delta < 0.0) {
                    textoBaskara = "Não existem raízes reais para " + a + "x² + " + b + "x + " + c + " = 0";
                } else {
                    double x1 = (-b - Math.sqrt(delta)) / (2 * a);
                    double x2 = (-b + Math.sqrt(delta)) / (2 * a);
                    textoBaskara = "Δ = " + delta + ", x' = " + x1 + " e x\" = " + x2 + " para " + a + "x² + " + b + "x + " + c + " = 0";
                }
            }
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");

            String textoHistorico = historico.getText();
            if (textoHistorico.equals("")) {
                historico.setText(textoBaskara);
            } else {
                String[] linhas = textoHistorico.split("\n");
                int limite = 5;
                if (linhas.length < limite) {
                    historico.setText(textoHistorico + "\n" + textoBaskara);
                } else {
                    String textoAtualizado = "";
                    for (int i = 0; i < limite - 1; i++) {
                        linhas[i] = linhas[i + 1];
                        textoAtualizado = textoAtualizado + linhas[i] + "\n";
                    }
                    textoAtualizado = textoAtualizado + textoBaskara;
                    historico.setText(textoAtualizado);
                }

                calculo.setText("");
            }
        }
    }//GEN-LAST:event_jButton25ActionPerformed

