    @SuppressWarnings("unchecked")
    public void Historico(double resultado) {
        String texto = calculo.getText();
        String textoHistorico = historico.getText();
        if (textoHistorico.equals("")) {
            historico.setText(texto + " = " + resultado);
        } else {
            String[] linhas = textoHistorico.split("\n");
            int limite = 5;
            if (linhas.length < limite) {
                historico.setText(textoHistorico + "\n" + texto + " = " + resultado);
            } else {
                String textoAtualizado = "";
                for (int i = 0; i < limite - 1; i++) {
                    linhas[i] = linhas[i + 1];
                    textoAtualizado = textoAtualizado + linhas[i] + "\n";
                }
                textoAtualizado = textoAtualizado + texto + " = " + resultado + "";
                historico.setText(textoAtualizado);
            }
        }
        calculo.setText("");
    }
